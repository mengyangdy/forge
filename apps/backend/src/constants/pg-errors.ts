/**
 * 默认数据库错误提示
 */
export const DEFAULT_DB_ERROR_MESSAGE = "数据库操作失败，请稍后重试。";

/**
 * 判断是否为数据库错误
 * @param err - 错误对象
 * @returns 是否为数据库错误
 */
export function isDatabaseError(err: any): boolean {
  return err && typeof err === "object" && "code" in err && err.code in PG_ERROR_MAP;
}

/**
 * 获取数据库错误的友好提示
 * @param err - 错误对象
 * @returns 用户友好的错误提示
 */
export function getDbErrorMessage(err: any): string {
  if (!err || typeof err !== "object" || !("code" in err)) {
    return DEFAULT_DB_ERROR_MESSAGE;
  }

  const pgCode = (err as any).code as string;
  return PG_ERROR_MAP[pgCode] || DEFAULT_DB_ERROR_MESSAGE;
}

/**
 * 获取数据库错误信息对象
 * @param err - 错误对象
 * @returns 包含是否为数据库错误和友好提示的对象
 */
export function getDbErrorInfo(err: any): { isDbError: boolean; message: string; code?: string } {
  if (!err || typeof err !== "object" || !("code" in err)) {
    return { isDbError: false, message: DEFAULT_DB_ERROR_MESSAGE };
  }

  const pgCode = (err as any).code as string;
  const isDbError = pgCode in PG_ERROR_MAP;

  return {
    isDbError,
    message: isDbError ? PG_ERROR_MAP[pgCode] : DEFAULT_DB_ERROR_MESSAGE,
    code: pgCode,
  };
}
